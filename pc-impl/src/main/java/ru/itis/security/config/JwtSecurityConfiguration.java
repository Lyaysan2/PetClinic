    private static final String[] PERMIT_ALL = {
            "/api/auth/**",
            "/api/token/**",
//            "/api/test",
            "/v3/api-docs/**",
            "/swagger-ui/**",
            "/swagger-ui.html",
            "/account-swagger/api-docs",
            "/api-docs/**",
            "/api-docs.yaml"
    };

    @Autowired
    private AccountUserDetailsService userDetailsService;

//    @Autowired
